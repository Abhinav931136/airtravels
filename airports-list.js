// Function to handle airport search for both fields
function setupAirportSearch(inputId, listId) {
    const searchInput = document.getElementById(inputId);
    const airportList = document.getElementById(listId);

    // Function to display filtered airports based on search input
    function displayAirports(filter) {
        // Clear the list
        airportList.innerHTML = '';

        // Only show the list if there's input
        if (filter.trim() === '') {
            airportList.style.display = 'none'; // Hide list if input is empty
            return;
        }

        // Filter the airports based on the name or IATA code
        const filteredAirports = airports.filter(airport =>
            airport.name.toLowerCase().includes(filter.toLowerCase()) ||
            airport.iata.toLowerCase().includes(filter.toLowerCase())
        );

        if (filteredAirports.length === 0) {
            airportList.style.display = 'none'; // Hide the list if no matches
            return;
        }

        // Append filtered airports (show full name and IATA code) to the list
        filteredAirports.forEach(airport => {
            const li = document.createElement('li');
            li.textContent = `${airport.name} (${airport.iata.toUpperCase()})`; // Display full name with IATA code

            // Add a click event listener to each airport in the list
            li.addEventListener('click', function () {
                // Set the IATA code as the value of the input field
                searchInput.value = airport.iata.toUpperCase();
                // Hide the list after selection
                airportList.style.display = 'none';
            });

            airportList.appendChild(li);
        });

        // Show the list once it has content
        airportList.style.display = 'block';
    }

    // Event listener to handle search input
    searchInput.addEventListener('input', (e) => {
        const searchValue = e.target.value;
        displayAirports(searchValue);
    });

    // Hide the list when clicking outside the input or list
    document.addEventListener('click', function (event) {
        if (!searchInput.contains(event.target) && !airportList.contains(event.target)) {
            airportList.style.display = 'none';
        }
    });
}

